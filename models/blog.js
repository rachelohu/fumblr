//////////////////////////////////////////////
// Import Dependencies
//////////////////////////////////////////////
const mongoose = require("./connection")

////////////////////////////////////////////////
// Define Model
////////////////////////////////////////////////
// pull schema and model from mongoose
const { Schema, model } = mongoose;

// make blogs schema
const blogsSchema = new Schema({
  name: String,
  text: String,
  public: Boolean,
  username: String
});

// make blog model
const Blog = model("Blog", blogsSchema);

///////////////////////////////////////////////////
// Export Model
///////////////////////////////////////////////////
module.exports = Blog